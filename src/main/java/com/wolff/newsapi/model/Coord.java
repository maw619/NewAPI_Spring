package com.wolff.newsapi.model;

public class Coord {

    private int lon;

    private int lat;

    public Coord(){

    }

    public int getLon() {
        return lon;
    }

    public void setLon(int lon) {
        this.lon = lon;
    }

    public int getLat() {
        return lat;
    }

    public void setLat(int lat) {
        this.lat = lat;
    }
}
